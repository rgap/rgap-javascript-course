// Create an array to hold squares.
let squares = [];
// Loop through numbers 0 to 9.
for (let x = 0; x < 10; x++) {
  // Compute the square of x.
  let square = x * x;
  // Store the square in the array.
  squares.push(square);
}

// Output the array to verify it contains the correct squares.
console.log(squares);
